function getLang(): string
{
    if (!empty($_GET['lang']) && in_array($_GET['lang'], ['en', 'so'], true)) {
        $_SESSION['lang'] = $_GET['lang'];
    }
    return $_SESSION['lang'] ?? 'en';
}

function __(string $key): string
{
    global $translations;
    $lang = getLang();
    return $translations[$lang][$key] ?? $translations['en'][$key] ?? $key;
}
